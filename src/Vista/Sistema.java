    private void btnExcelProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcelProductoActionPerformed
        Path currentDirectoryPath = FileSystems.getDefault().getPath("");
	String ruta = currentDirectoryPath.toAbsolutePath().toString();
        try {
            File archivoXLS = new File(ruta+"\\Reporte.xls");
            if (archivoXLS.exists()) {
                archivoXLS.delete();
            }
            archivoXLS.createNewFile();
            Workbook libro = new HSSFWorkbook();
            FileOutputStream archivo = new FileOutputStream(archivoXLS);
            Sheet hoja = libro.createSheet("Reporte");
            hoja.setDisplayGridlines(false);
                for (int f = 0; f < TableProducto.getRowCount(); f++) {
                    Row fila = (Row) hoja.createRow(f);
                    for (int c = 0; c < TableProducto.getColumnCount(); c++) {
                        Cell celda =fila.createCell(c);
                        if (f == 0) {
                            celda.setCellValue(TableProducto.getColumnName(c));
                        }
                    }
                }
                int filaInicio = 1;
                for (int f = 0; f < TableProducto.getRowCount(); f++) {
                    Row fila = (Row) hoja.createRow(filaInicio);
                    filaInicio++;
                    for (int c = 0; c < TableProducto.getColumnCount(); c++) {
                        Cell celda = fila.createCell(c);
                        if (TableProducto.getValueAt(f, c) instanceof Double) {
                            celda.setCellValue(Double.parseDouble(TableProducto.getValueAt(f, c).toString()));
                        } else if (TableProducto.getValueAt(f, c) instanceof Float) {
                            celda.setCellValue(Float.parseFloat((String) TableProducto.getValueAt(f, c)));
                        } else {
                            celda.setCellValue(String.valueOf(TableProducto.getValueAt(f, c)));
                        }
                    }
                }
                libro.write(archivo);
                archivo.close();
                Desktop.getDesktop().open(archivoXLS);
            } catch (IOException | NumberFormatException e) {
                
            }
 
    }//GEN-LAST:event_btnExcelProductoActionPerformed

