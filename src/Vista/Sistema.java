    private void RegistrarVenta() {
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/YYYY");
        String fechaventa=formatoFecha.format(jDateChooser1.getDate());
        String vendedor = LabelVendedor.getText();
        double monto =Double.parseDouble(LabelTotal.getText());
        ventas.setRucdnicliente(txtRucVenta.getText());
        ventas.setNombre_cli(txtNombreClienteVenta.getText());
        ventas.setVendedor(vendedor);
        ventas.setTotal(monto);
        ventas.setFecha(fechaventa);
        ventasdao.RegistrarVenta(ventas);
    }

    private void RegistrarDetalle() {
        int id = ventasdao.IdVenta();
        for (int i = 0; i < TableVentas.getRowCount(); i++) {
            int id_pro = Integer.parseInt(TableVentas.getValueAt(i, 0).toString());
            int cant = Integer.parseInt(TableVentas.getValueAt(i, 2).toString());
            double precio = Double.parseDouble(TableVentas.getValueAt(i, 3).toString());
            detalle.setId_pro(id_pro);
            detalle.setCantidad(cant);
            detalle.setPrecio(precio);
            detalle.setId(id);
            ventasdao.RegistrarDetalle(detalle);

        }
    }

    private void ActualizarStock() {
        for (int i = 0; i < TableVentas.getRowCount(); i++) {
            int id = Integer.parseInt(TableVentas.getValueAt(i, 0).toString());
            int cant = Integer.parseInt(TableVentas.getValueAt(i, 2).toString());
            productos = productosdao.BuscarId(id);
            int StockActual = productos.getStock() - cant;
            ventasdao.ActualizarStock(StockActual, id);

        }
    }
        
